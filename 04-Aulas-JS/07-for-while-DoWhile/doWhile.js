// Faça isso, enquanto aquilo for verdade.
/*
USOS:
do...while: Quando precisa executar pelo menos uma vez, mesmo que a condição seja falsa.
Exemplo: Solicitar senha até acertar, mas sempre pede pelo menos uma vez.

*/

let i = 0;

do {  // faça isso
    console.log(`incrementando a variavel ${i}`);
    i++;
}while(i <= 5); //enquanto isso for verdade




