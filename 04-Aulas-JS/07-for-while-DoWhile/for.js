/*

A sintaxe do for tem 3 partes:

1. Inicializador: onde começa a contagem (geralmente uma variável "0").
2. Condição: Até quando o loop deve continuar
3. Incremento: aumenta +1 a cada volta no looping

for(inicializador; condição; incremento){
    // código a ser executado
}

USOS:
for: Quando você sabe o número exato de repetições.
Exemplo: Listar produtos de um carrinho

*/

for(let i = 0; i <= 5; i++){
    console.log(`Incrementando a variavel i para: ${i}`);
}

