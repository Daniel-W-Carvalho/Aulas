/*
USOS:
while: Quando não sabe quantas vezes vai repetir, depende de uma condição.
Exemplo: Ler dados enquanto o usuário não digita "sair".

*/


let numero0 = 0;
const numero10 = 10;


// enquanto numero0 for menor ou igual a numero10, faça:
while (numero0 <= numero10){
    console.log(`Incrementando a variavel numero0 ${numero0}`);
    numero0++; // numero0 = numero0 + 1
}



