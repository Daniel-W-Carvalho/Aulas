// EVENTOS DE MOUSE
// click -> Quando o usuário clica (o mais usado de todos).,"Botões, links, ícones de fechar."
// dblclick -> Clique duplo.,"Editar um campo de texto ou dar ""like"" em fotos (estilo Instagram)."
// mouseenter -> Quando o mouse entra na área do elemento.,Mostrar um menu suspenso ou dar zoom em uma foto.
// mouseleave -> Quando o mouse sai da área do elemento.,Esconder um menu ou remover um efeito visual.

//EVENTOS DE TECLADO(ENTRADA DE DADOS)
// keydown -> No momento em que a tecla é pressionada.,Atalhos (como ESC para fechar um modal) ou movimentar personagens em jogos.
// keyup -> Quando o usuário solta a tecla.,Filtros de busca que atualizam a lista enquanto você digita.

//EVENTOS DE FORMULÁRIO (DADOS E ENVIO)
// submit -> Quando o formulário é enviado.,Validar dados antes de mandar para o servidor (usando o preventDefault).
// input -> Sempre que o valor de um campo muda.,Validar a força de uma senha em tempo real.
// focus -> Quando o usuário clica dentro de um campo.,Mudar a cor da borda do input para destacar onde o usuário está.
// blur -> Quando o usuário sai do campo (perde o foco).,Verificar se o e-mail digitado é válido logo após o usuário sair do campo.

//EVENTOS DE JANELAS/DOCUMENTO(GLOBAIS)
// DOMContentLoaded -> Quando o HTML foi totalmente carregado.,Garantir que o JS só rode depois que todos os elementos existam.
// scroll -> Quando o usuário rola a página.,"Botão de ""Voltar ao topo"" ou animações que surgem conforme você desce o site."

//OBSERVAÇÕES
// O evento input dispara a cada letra que você digita.
// O evento change só dispara quando você termina de digitar e clica fora do campo.